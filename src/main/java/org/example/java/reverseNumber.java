package org.example.java;

public class reverseNumber {

    public static void main(String[] args) {


        // print 1-20
        for (int i=1; i<=20; i++)
        {
            System.out.print(i+" ");

        }
        System.out.println();

        // print 20-1
        for (int i=20; i>=1; i--)
        {
            System.out.print(i+" ");
        }
    }
}
